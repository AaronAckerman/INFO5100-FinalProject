    public void displayUser() {
        User user = jFrame.getUser();
        Role role = jFrame.getRole();

        if (user != null || role != null) {
            logoutjButton.setEnabled(true);
        }

        if (user == null) {
            usernamejTextField.setText("");
        } else {
            usernamejTextField.setText(user.getUsername());
        }

        if (role == null) {
            rolejTextField.setText("");
            organizationjTextField.setText("");
            enterprisejTextField.setText("");
        } else {
            rolejTextField.setText(role.toString());
            organizationjTextField.setText(role.getOrganization().toString());
            enterprisejTextField.setText(role.getOrganization().getEnterprise().toString());
        }

        if (user == null && role == null) {
            logoutjButton.setEnabled(false);
        }
    }

